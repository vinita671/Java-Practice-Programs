package operators;

public class Relational {
    public static void main(String[] args) {
        
        System.out.println(10 < 20);     // true
        System.out.println(10 > 20);     // false
        System.out.println(10 <= 20);    // true
        System.out.println(10 >= 20);    // false
        System.out.println(10 == 20);    // false
        System.out.println(10 != 20);    // true
        System.out.println(10 + 20 > 20);        // true
        System.out.println(10 + 20 == 20 + 10);  // true
    }  
}   


